#include "../common.h"

void test_tcp_checksum()
{
    U16 checksum = 0xb5d4;
    char ip_pkt[] = {0x45, 0x00, 0x00, 0x34, 0x00, 0x00, 0x40, 0x00, 0x40, 0x06, 
    0x28, 0xd6, 0xc0, 0xa8, 0xc8, 0x15, 0xc0, 0xa8, 0xc8, 0x87};
    char tcp_pkt[] = {0x07, 0xd0, 0x1a, 0x37, 0x1f, 0x5f, 0xd2, 0x50, 0xdd, 0x6a,
    0xbb, 0x2b, 0x80, 0x12, 0xfa, 0xf0, 0xb5, 0xd4, 0x00, 0x00, 0x02, 0x04, 0x05,
    0xb4, 0x01, 0x01, 0x04, 0x02, 0x01, 0x03, 0x03, 0x07};

    assert(CHKSUM_TCP((tIP_PKT *)ip_pkt, (tTCP_PKT *)tcp_pkt) == checksum);
}