#include "../common.h"

void test_tcp_checksum()
{
    U16 checksum = htons(0xb5d4);
    char ip_pkt[] = {0x45, 0x00, 0x00, 0x34, 0x00, 0x00, 0x40, 0x00, 0x40, 0x06, 
    0x28, 0xd6, 0xc0, 0xa8, 0xc8, 0x15, 0xc0, 0xa8, 0xc8, 0x87};
    char tcp_pkt[] = {0x07, 0xd0, 0x1a, 0x37, 0x1f, 0x5f, 0xd2, 0x50, 0xdd, 0x6a,
    0xbb, 0x2b, 0x80, 0x12, 0xfa, 0xf0, 0xb5, 0xd4, 0x00, 0x00, 0x02, 0x04, 0x05,
    0xb4, 0x01, 0x01, 0x04, 0x02, 0x01, 0x03, 0x03, 0x07};

    assert(CHKSUM_TCP((tIP_PKT *)ip_pkt, (tTCP_PKT *)tcp_pkt) == checksum);

    checksum = htons(0xa9ab);
    char ip_pkt2[] = {0x45, 0x00, 0x00, 0x2e, 0x1b, 0x65, 0x40, 0x00, 0x80, 0x06, 
    0xcd, 0x76, 0xc0, 0xa8, 0xc8, 0x87, 0xc0, 0xa8, 0xc8, 0x15};
    char tcp_pkt2[] = {0x1a, 0x37, 0x07, 0xd0, 0xdd, 0x6a, 0xbb, 0x2b, 0x1f, 0x5f, 
    0xd2, 0x51, 0x50, 0x18, 0x04, 0x02, 0xa9, 0xab, 0x00, 0x00, 0x68, 0x65, 0x6c,
    0x6c, 0x6f, 0x0a};

    assert(CHKSUM_TCP((tIP_PKT *)ip_pkt2, (tTCP_PKT *)tcp_pkt2) == checksum);
}